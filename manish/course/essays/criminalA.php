<p>Question</p>
<p>Arthur hears that an expensive yacht will be moored in City Harbor with only an old watchman on board. Arthur asks Ben and Carl to help him steal whatever is valuable on the yacht. The three agree to board the yacht at night under the cover of darkness. </p>
<p dir="ltr">Carl buys a gun &ldquo;just in case&rdquo; but does not tell Arthur and Ben. </p>
<p dir="ltr">Arthur, Ben and Carl drive to City Harbor, but when they get there, Ben decides the plan is too risky and decides to go home. </p>
<p dir="ltr">Arthur and Carl board the yacht and steal several thousand dollars worth of diamonds. As they are leaving the yacht, Arthur breaks a mirror by accident which wakes up the night watchman. In a panic, Carl shoots at the watchman, who dies instantly of the gunshot wound. </p>
<p dir="ltr">In the meantime, on his way home, Ben is so upset about the events that he stops at a bar and drinks for several hours. Ben then gets into his car, runs a red light, and kills Sally who was walking home. </p>
<p dir="ltr">When Tiffany, the watchman&rsquo;s wife, hears that her husband was killed, she drops dead of a heart attack. </p>
You are assigned by the State to prosecute Arthur, Ben and Carl. What crimes will you charge them with and what defenses will they raise?
