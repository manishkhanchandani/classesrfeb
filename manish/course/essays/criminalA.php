<p>Notes</p>
<ul>
  <li>Arthur hears that an expensive yacht will be moored in City Harbor with only an old watchman on board. (intent)</li>
  <li>Arthur asks Ben and Carl to help him steal whatever is valuable on the yacht. (Solicitation &amp; conspiracy)</li>
  <li>The three agree to board the yacht at night under the cover of darkness. (burglary)</li>
  <li>Carl buys a gun &ldquo;just in case&rdquo; but does not tell Arthur and Ben. </li>
  <li>Arthur, Ben and Carl drive to City Harbor, but when they get there, Ben decides the plan is too risky and decides to go home. (withdrawal from conspiracy)</li>
  <li>Arthur and Carl board the yacht and steal several thousand dollars worth of diamonds. (larceny or burglary)</li>
  <li>As they are leaving the yacht, Arthur breaks a mirror by accident which wakes up the night watchman.</li>
  <li>In a panic, Carl shoots at the watchman, who dies instantly of the gunshot wound. (felony murder - first degree)</li>
  <li>In the meantime, on his way home, Ben is so upset about the events that he stops at a bar and drinks for several hours.</li>
  <li>Ben then gets into his car, runs a red light, and kills Sally who was walking home. (involuntary manslaughter)</li>
  <li>When Tiffany, the watchman&rsquo;s wife, hears that her husband was killed, she drops dead of a heart attack. </li>
</ul>
