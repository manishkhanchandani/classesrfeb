  <li><strong>Liability of City Middle School</strong>
<ol>
      <li><strong>Vicarious Liability</strong><br>
      Liability that a supervisory party bears for the actionable conduct of a subordinate or associate because of the relationship between the two parties.      <br>
      <br>
      This rule applies whether the acts were committed in the employer&rsquo;s presence or not. The crucial issue in invoking respondeat superior is determining whether or not the employee&rsquo;s actions were committed within the scope of his employment (whether or not the employee&rsquo;s actions were committed while he was doing his job). Employers are not liable for their employees&rsquo; tortious or negligent actions if the actions were committed outside the scope of the employees&rsquo; employment.<br>
      <br>
      Here teacher took students to field trip within the scope of City Middle School and so City Middle school is responsible for all the actions committed by teacher.
<br>
      <br>
      If Teacher is found to be negligent and City Middle School is an employer of teacher, then City Middle School could be found vicariously liable under the doctrine of Respondeat Superior, for Teacher's negligence. <br>
      <br>
      Jennifer will be able to sue Teacher&rsquo;s employer i.e. City Middle School for negligence.<br>
