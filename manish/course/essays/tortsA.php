<p>Question</p>
<p>Teacher teaches science at City Middle School and, as is customary near the end of the term, he takes his 7th grade biology class on a field trip to the beach. All the necessary forms and waivers have been signed by the students, and after Teacher admonishes them to be careful, they begin exploring the beach while he lies down to relax in the sun.</p>
<p>In a tide pool, Jennifer sees the most beautiful sea urchin. However, when she picks it up, one of its spines dislodges in her finger. Afraid that she will get in trouble, Jennifer says nothing to Teacher or her parents, until several days later when the swelling becomes more noticeable. At this point, she tells Teacher.</p>
<p>Teacher explains the danger of infection and tells Jennifer to be sure to tell her parents, which she does several days later. However, Jennifer’s parents strongly believe in the healing power of sunlight, and after two more days of keeping her finger in the sun, a serious infection develops and Jennifer’s finger needs to be amputated.</p>
<p>Discuss the potential liability of Teacher and of City Middle School, as well as any defenses each may raise.<br>
</p>
