<ol>
  <li><strong>Liability of Teacher</strong>
<ol>
      <li><strong>Negligence</strong><br>
         The four elements that a plaintiff must prove to win a negligence suit are 1) Duty, 2) Breach, 3) Cause, and 4) Harm. 
         <ol>
          <li><strong>Duty</strong><br>
            A duty is simply a legal obligation. In order to be sued for Negligence, the Defendant must have owed a duty to the Plaintiff.
              <br>
            <strong>Standard of Care/General duty</strong><br>
           The standard of care used to determine general duty of care is one exercised by a reasonable person of ordinary prudence under the same or similar circumstances.          <br>
           <br>
           Here teacher took students for the field trip and it becomes general duty of teacher to provide standard care to the students. Teacher has to take care of students and make sure that students should not cross lines marked by teacher. Waiver form and warning to students does not mean that teacher does not own a duty.<br>
           <br>
           Hence, there is a duty existed between teacher and student.
           <br>
           <br>
           </li>
          <li><strong>Breach</strong><br>
          A breach is a violation of a law or duty. The Defendant must breach his duty in order to be liable for negligence.          <br>
          <br>
          Facts says that &quot;All the necessary forms and waivers have been signed by the students, and after Teacher admonishes them to be careful, they begin exploring the beach while he lies down to relax in the sun.&quot; It is true that all waiver forms were signed and teacher also warns students to be careful, but teacher lies down in the sun to relex and he neglects the students. It becomes duty of teacher to be careful and watch the students. <br>
          <br>
          Due to this, teacher breached the duty of standard care.
          </li>
          <li><strong>Cause</strong><br>
            The breach of duty must have caused harm to the Plaintiff.
            <ol>
              <li><strong>Actual Cause</strong><br>
              An act whose direct result is the plaintiff&rsquo;s harm.              <br>
              <strong>&quot;But For&quot;</strong><br>
The method of determining the cause without which the plaintiff&rsquo;s harm would not have occurred. <br>
              <br>
              Since teacher wents to relax under the sun, students tried to explore the beach and one of spine dislodges in finger of Jennifer. Here because teacher breached the duty and he was relaxing in the beach and because of that urchin causes harm to Jennifer,<br>
              <br>
              Hence Teacher was direct result in plaintiff's harm. <br>
              <br>
              Here we can use &quot;But For&quot; test to conclude that teacher was responsible for the Jennifer's harm.</li>
              <li><strong>Proximate Cause</strong><br>
                Cause that is legally sufficient to result in liability.                 Proximate cause is present when the harm suffered is a foreseeable consequence of the defect with no intervening acts breaking the chain of causation.<br>
                <br>
                Proximate cause refers to the legal liability of Teacher as to whether he is directly liable for Jennifer's injuries, without any supervening acts. Teacher breached the duty and because of that Jennifer got injured by urchin. But whether jennifer's amputation was direct result of breach by teacher? Answer is no since there was time gap between amputation and injury and Jennifer did not told the injury to teacher and when she says that to teacher, she again did not said this to her parents immediately. <br>
                <br>
                But teacher can argue that because of intervening act i.e. she did not said this to parents and because of that there was sufficient time gap which prevents Teacher to be at fault. <br>
                <br>
Hence Teacher may not be fully responsible for the jennifer's amputation. <br>
                <br>
                <strong>Foreseeability:</strong><br>
                An expected outcome of the defendant's acts.  <br>
                <br>
                The general rule is that, where the results are foreseeable, liability usually attaches because, if the defendant causes a foreseeable injury, this usually presents a clear enough case of liability that proximate cause is rarely in doubt.<br>
                <br>
                Major thing can happen if teacher does not take care of students in the beach and after injury of Jennifer. Here it is forseeable that major thing can happen after the injury of Jennifer. 
Here Jennifer suffered injury which are foreseeable to the circumstances. And that injury can go to any extent. <br>
<br>
Hence Teacher is liable for negligence to Jennifer.<br>
<br>
              </li>
            </ol>
          </li>
          <li><strong>Damages</strong><br>
           The Plaintiff must suffer harm in order to sue for negligence. If he suffers no harm, he cannot sue.          <br>
           <br>
           Here jennifer suffered the damage by amputation of her hand.
          <br>
          <br>
          Hence Teacher is laible for the negligence of Jennifer's amputation of hand.
          </li>
          <li><strong>Defenses</strong>
<ol>
              <li><strong>Contributory Negligence</strong><br>
              Conduct on the part of the plaintiff that contributes to the events leading to his injuries.              <br>
              <br>
              The basis for this defense is that everybody has a duty to avoid injury at the hands of another and, if you fail to protect your own safety, you may be barred from recovering for your injuries.              Contributory negligence is a complete bar to recovery.<br>
              <br>
              Here Jennifer did not told her injury to teacher for few days and after teacher asked her to tell her parents, which she does several days later. Moreover, Jennifer's parents did not took the Jennifer to the doctor, instead they waited two more days for exposure to sunlight which increased the infection. Teacher would argue that this as her last clear chance to avoid the harm. This shows that Jennifer fails to protect herself and <br>
              <br>
              Therefore teacher is not responsible for the condition of Jennifer.</li>
              <li><strong>Comparative Negligence</strong><br>
                The principle that reduces a plaintiff&rsquo;s recovery proportionally to the plaintiff&rsquo;s degree of fault in causing the damage.<br>
                <br>
                Here facts states that plantiff i.e. Jennifer did all mistake by not telling her problem to teacher and parents and morever after she said her problem to parents, parents did not took her to doctor but waited 2 more days. This clearly shows that jennifer was completely responsible for her injuries.<br>
                <br>
                So comparative negligence is not needed in this case as Teacher is not responsible based on contributory negligence.
<br>
              </li>
              <li><strong>Assumption of Risk</strong><br>
              Where the plaintiff has either explicitly or implicitly consented to the actions for which he is suing the defendant.<br>
              <br>
              Here facts says &quot;All forms and waivers are signed&quot;, so Jennifer has 
              signed waiver form and that means she knows all the risks of attending the field trip.<br>
              <br>
              Hence Jennifer is considered to have assumed the risk and is thus barred from any recovery for negligence.</li>
            </ol>
          </li>
        </ol>
        <br>
    <br>
  </li>
</ol>
