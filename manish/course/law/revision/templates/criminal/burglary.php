
<h1>Burglary</h1>

<p>Burglary, <u>at common law</u>, is the <em>Breaking</em> and <em>entering</em> the<em> dwelling house</em> of another, at night, with the <u>specific intent to commit a felon</u>y therein.</p>
<h3>Modern Law for Burglary:
</h3>
<p>Trespassory entry into any structure with intent to commit a crime therein. </p>
<p><strong>Template</strong></p>
<p>Breaking: A and B gained entry to Rich's house by cutting a hole in the back door with a blowtorch. This constitutes a breaking because they made the hole.</p>
<p>Entering: The facts state that A and B entered Rich's house through the hole they had made in the back door.</p>
<p>Dwelling: A and B broke into and entered Rich's home, which is a dwelling</p>
<p>Night: The facts state B brought a blowtorch with him &quot;that night&quot; which indicates the burglary occured at nighttime.</p>
<p>Intent to commit a Felony: The facts state A and B intended to steal valuable intique coins. It is likely that the value of the coins was over $500, which means that stealing them would be a felony, so intent to commit a felony is established. Even if it turns out the coin's value were less than $500, most jurisdictions have statutorily modified the intent required for burglary to include intent to commit any theft, including pretty theft.</p>
<p><strong>Conclusion:</strong><br>
Therefore, A and B can be charged with burglary.</p>
