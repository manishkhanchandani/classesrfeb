<h1>Homicide</h1>
<p>Killing of <u>one human being by another human being</u>. </p>
<p>Analysis: S died as a result of the fire started by A and B and is therefore a homicide.</p>
<p><strong>Causation</strong></p>
<p><strong>Actual Cause</strong><br>
An actual cause is the cause which starts, ignites or makes possible the result which follows, and which satisfies “But For” or “Substantial Factor Test”<br>
“But For”: The “But For” Test is used to establish actual cause in criminal cases which include a harmful result as an element of the crime. <br>
To apply the test, the state must show that “but for” the defendant's act, the harm would not have occurred.
<br>
<br>
“Substantial Factor Test”: The Substantial Factor Test is used to establish actual cause where more than one act contributes to the harm. <br>
The defendant is said to be an actual cause of the harm if the defendant’s act is a substantial factor in bringing the harm about. This means that the defendant's act contributed to more than a trivial degree to the resulting harm.</p>
<p>Template</p>
<p>&quot;But for&quot; the fire started by A and B, Sam would not have died. Therefore, the fire was an actual cause of Sam's death. If the court determines, as is likely according to the discussion of arson supra, A and B will be deemed to be actual cause of Sam's death.</p>
<p><strong>Proximate Cause</strong><br>
 (Was D’s act the proximate cause of Victim’s death?)
  <br>
  In criminal law, an actual cause of harm is also the proximate cause if the act is closely connected enough to the resulting harm that it is fair to hold the defendant responsible for causing the harm. <br>
  Under the Model Penal Code, this means that the resulting harm is not “too remote or accidental” from the defendant’s act to make it unfair to hold him responsible.</p>
<p>Template</p>
<p>As discussed under arson, the fire was a <u>forseeable result</u> of A and B's burglary and attempted larceny. However, A and B will argue that Carol's refusal to wake Sam was an <u>intervening act</u> which breaks the chaing of causation. However, since it would have been just as foreseeable for Sam to have been alone in his home with no one available to waken him, and since it would have been foreseeable for Carol to have also died, the lack of awakening Sam will not break the chan of causation and A and B will be held to be the <u>proximate cause</u> of Sam's death. </p>
<p><strong>Murder</strong> (Was the homicide committed with malice, and therefore a murder?)<br>
<br>


Murder is a homicide committed with malice aforethought.<br>
<br>


Malice can be shown in one of the four way: <br>

	a. an intent to kill (expressed by defendant)<br>

	b. an intent to cause serious bodily harm (implied by action)<br>

	c. a wanton and willful disregard of human life (implied by action)<br>

	d. an intent to commit a dangerous felony (implied by action)</p>
<p>Template</p>
<p>Malice aforethought can be found through the commission of a<strong><em> dangerous felony</em></strong>, so A and B will be charged with the murder of Sam. </p>
<p><strong>Murder in First Degree</strong></p>
<p>First degree murder is murder committed by poison; torture; lying in wait; or other willful, deliberate and premeditated means; or murder that results from application of the Felony Murder Rule.</p>
<p>Template</p>
<p>Burglary is considered to be a <strong>dangerous felony</strong>, which can be used for application of <strong>felony murder rule</strong>. The death must also be found to be <em><strong>causally connected</strong></em> to the burglary. This is satisfied in this case because if A and B had not burglarized Rich's home with the blowtorch, the fire would not have started. Therefore, the death of Sam is directly attributable to the burglary and the felony murder rule will attach.</p>
<p>Therefore, A and B can be charged with the murder in the first degree of Sam.  </p>
