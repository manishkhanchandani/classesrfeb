
<h1>Non Homicide</h1>
<p><strong>Assault</strong></p>
<p>An assault is an act which causes an immediate apprehension of a harmful or offensive contact.</p>
<p>Template</p>
<p>When A forced B to the ground, pulled a knife, and slit his nose, A's actions were intentional and placed B in fear of a harmful touching. A is guilty of assault. </p>
<p><strong>Battery</strong></p>
<p>Battery is the intentional, harmful or offensive touching of another person.</p>
<p>Template</p>
<p>When A pointed the gasoline dispenser at B and then sprayed her, soaking her hair and clothes, she was guilty of a battery because her act of pulling the trigger of the dispenser while pointing it at B shows her intent. </p>

