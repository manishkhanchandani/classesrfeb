    <td valign="top"><strong>Proximate Cause</strong></td>
    <td valign="top"><p>(Was D&rsquo;s act the proximate cause of Victim&rsquo;s death?) <br>
      In criminal law, an actual cause of harm is also the proximate cause if the act is closely connected enough to the resulting harm that it is fair to hold the defendant responsible for causing the harm. <br>
    </p>
    <p>Under the Model Penal Code, this means that the resulting harm is not &ldquo;too remote or accidental&rdquo; from the defendant&rsquo;s act to make it unfair to hold him responsible.</p></td>
    <td valign="top">As discussed under arson, the fire was a <u>forseeable result</u> of A and B's burglary and attempted larceny. However, A and B will argue that Carol's refusal to wake Sam was an <u>intervening act</u> which breaks the chaing of causation. However, since it would have been just as foreseeable for Sam to have been alone in his home with no one available to waken him, and since it would have been foreseeable for Carol to have also died, the lack of awakening Sam will not break the chan of causation and A and B will be held to be the <u>proximate cause</u> of Sam's death. </td>
