    <td valign="top"><strong>Actual Cause</strong></td>
    <td valign="top"><p>An actual cause is the cause which starts, ignites or makes possible the result which follows, and which satisfies &ldquo;But For&rdquo; or &ldquo;Substantial Factor Test&rdquo;</p>
      <p>&ldquo;But For&rdquo;: The &ldquo;But For&rdquo; Test is used to establish actual cause in criminal cases which include a harmful result as an element of the crime. </p>
      <p>To apply the test, the state must show that &ldquo;but for&rdquo; the defendant's act, the harm would not have occurred. <br>
        <br>
&ldquo;Substantial Factor Test&rdquo;: The Substantial Factor Test is used to establish actual cause where more than one act contributes to the harm. <br>
The defendant is said to be an actual cause of the harm if the defendant&rsquo;s act is a substantial factor in bringing the harm about. This means that the defendant's act contributed to more than a trivial degree to the resulting harm.</p></td>
    <td valign="top">&quot;But for&quot; the fire started by A and B, Sam would not have died. Therefore, the fire was an actual cause of Sam's death. If the court determines, as is likely according to the discussion of arson supra, A and B will be deemed to be actual cause of Sam's death.</td>
    <td valign="top">&nbsp;</td>
    <td valign="top">&nbsp;</td>
  </tr>
