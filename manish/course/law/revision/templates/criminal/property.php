
<h1>Crime Against Property</h1>
<p><strong>Larceny</strong></p>
<p>Trespassory taking and carrying away the personal property of another without consent, with specific intent to permanently deprive.</p>
<p>Template</p>
<p>When A took B's cash, he did not have B's consent. His act constituted a trespassory taking of the money. The physical taking of the money was an asportation which constituted a carraying away. Since the money belong to B, it was the personal property of another. When A ran to the car with the money, his conduct demonstrated his specific intent to permanently deprive B of his money.</p>
<p>A is guilty of larceny.</p>

