<h3>Issue: Solicitation (Oct 2003 95 marks)</h3>
<p><strong>Rule</strong><br>
Intent to induce another to commit a crime.</p>
<p><strong>Analysis</strong><br>
When A &quot;<u>asked B</u>&quot; to help him with &quot;<u>the heist</u>&quot;, A <u>committed a solicitation</u> because he requested B to participate in a burglary and larceny. </p>
<p><strong>Conclusion:</strong><br>
  A committed crime of solicitation.
</p>
<h3>Merger Rule  (Oct 2003 95 marks)</h3>
<p><strong>Rule</strong><br>
  In criminal law, if a defendant commits a single act that simultaneously fulfills the definition of two separate offenses, merger will occur. 
</p>
<p><strong>Analysis</strong><br>
Solicitation merges with the target crime i.e with attempt and conspiracy. Therefore, although A committed solicitation, he will be charged instead with <u>conspiracy</u> and with the other crimes discussed infra. </p>
<h3>Issue: <u>Conspiracy</u>  (Oct 2003 95 marks)</h3>
<p><strong>Rule</strong><br>
An <u>agreement</u> between <u>two</u> or <u>more</u> individuals to <u>commit an unlawful</u> act or a lawful act by unlawful means.</p>
<p><strong>Analysis</strong><br>
When B &quot;agreed&quot; to A's solicitation, a conspiracy was formed and both A and B may be charged with conspiracy to commit &quot;crime 1&quot; and conspiracy to commit &quot;crime 2&quot;</p>
<p><strong>Conclusion</strong><br>
A and B guilty of conspiracy.</p>
<p>&nbsp;</p>
<h3>Examples</h3>
<p><strong>Conspiracy between two persons with one unknown to the intent of other? October 2007</strong></p>
<p>The facts indicate that Pete and Frank agreed to the purchase of the property. However, since Frank had no knowledge of the purpose and intent of Pete to commit a fraud upon Vic, under Wharton's Rule, two or more individuals would be needed in order to prevail on a conspiracy charge.</p>
<p>Therefore, at common law, there would be in conspiracy. However, modernly, applying the unilateral approach, there would be a conspiracy. Depending upon the jurisdiction, this charge may be viable as to Pete.</p>
<p><strong>No necessary mens rea? June 2006</strong></p>
<p>Dan conspired with fred to go to Bess's home to see what Bess was up to. It does not appear at this time Dan had the necessary mens rea to commit an unlawful act.</p>
<p>Therefore, he will not be charged with conspiracy.</p>
<p><strong>Rob a convenience store? June 2004</strong></p>
<p>Art conspired with Ben to commit a robbery when they agreed that they would rob an all night convenience store. Therefore, Art can be convicted of conspiracy for his agreement to work with Ben to rob a convenience store.</p>
