  <li><strong>Theories (Can A prevail under the products theory of battery, negligence, breach of warranty, strict liability in torts)?</strong>
<ol>
      <li><strong>Battery</strong><br>
        In order for the plaintiff to recover on a theory of intent (battery), the plaintiff must prove that the manufacturer, distributor or supplier knew the product would cause harm.        <br>
      </li>
      <li><strong>Negligence</strong><br>
        In order for a plaintiff to recover under a negligence theory of products liability, the plaintiff will have to prove duty, breach, causation and damages.
<ol>
          <li><strong>Duty</strong><br>
            Originally extended only to those in privity of contract, but under MacPherson v. Buick, the duty was extended to all foreseeable users.            <br>
          </li>
          <li><strong>Breach</strong><br>
          P must show the product failed to meet ordinary commercial expectation          </li>
          <li><strong>Causation</strong><br>
            P must show that D was the actual and proximate cause.
            <ol>
              <li>Actual Cause</li>
              <li>Proximate Cause<br>
              </li>
            </ol>
          </li>
          <li><strong>Damages</strong><br>
          Limited to personal and property. Punitive available for reckless conduct.          </li>
          <li><strong>MacPherson v. Buick</strong> <br>
          In this landmark case, Justice Cardozo dispensed with the previously applied requirement of privity of contract in products liability cases and extended the manufacturer’s duty of care to include the ultimate consumer of the product, whether or not the consumer was the actual purchaser of the product. Later cases extended the duty to include not just the purchaser or the ultimate consumer, but to include all persons and property likely to be endangered by the product's probable use, such as the consumer's family or guests, or even near bystanders.</li>
        </ol>
      </li>
      <li><strong>Breach of Warranty</strong><br>
        Liability can be imposed against the manufacturer or seller of a product based upon the buyer’s <strong>reliance</strong> upon <strong>express and / or implied warranties</strong> that the goods are of <strong>merchantable quality</strong> and are fit for their <strong>intended purpose</strong>.
        <ol>
          <li>IRAC Breach of Warranty
            <ol>
              <li><strong>Implied promise</strong> by a merchant selling goods that product is fair and average quality, </li>
              <li><strong>Fit </strong>for normal use</li>
              <li>Emphasis is on <strong>safety</strong><br>
                <strong>Example: </strong>The implied warranty relating to a car would likely include that its safety devices (airbag) would protect, not harm, the occupants. A impliedly warranted that the airbag was safe when it sold the car with this safety device.
              Moreover, the addition of the inexpensive safety switch would have added to the reasonableness of the airbag's safety.</li>
              <li><strong>Intended Purpose</strong><br>
                Example: If the seller knows of buyer's intended use, and buyer acting as an average reasonable person, relies upon seller's &quot;special knowledge and skills,&quot; then a breach has occured.
              </li>
              <li><strong>Breach of Warranty</strong>, why it is not safe?<br>
              <strong>Example: </strong>A breached the warranty by equipping the Roadster with an airbag that created a danger to young passengers. </li>
              <li><strong>Privity of contract</strong><br>
              <strong>Example: </strong>Most courts hold that the privity of contract is not required to recover in tort for breach of the implied warranty. A user of a product, like C, can sue, even though she did not purchase the car. </li>
              <li><strong>Causation</strong><br>
                Example: As discussed, breach was the cause in fact and proximate cause of C's head injury.</li>
              <li>Damages</li>
              <li>Defenses<br>
                Example: No defenses.
              </li>
            </ol>
          </li>
        </ol>
      </li>
      <li>Strict Liability  </li>
