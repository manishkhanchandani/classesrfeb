    <td valign="top">3. False Imprisonment</td>
    <td valign="top">3, 4, 5, 8, 11, 16</td>
    <td valign="top"><div title="Page 69">
      <div>
        <div>
          <p>Under tort law a false imprisonment is an <strong>intentional act</strong> causing the plaintiff to be <strong>confined to a defined space </strong>against their will with<strong> no reasonably apparent means of reasonable exit</strong>. The plaintiff must be aware of the confinement. </p>
        </div>
      </div>
    </div></td>
    <td valign="top"><div title="Page 69">
      <div>
        <div>
          <p>Here Kent <strong>intentionally acted </strong>because it was a &ldquo;prank&rdquo;, and he <strong>confined</strong> Gates with <strong>no reasonably apparent means of reasonable exit</strong> because he put him on a &ldquo;ledge&rdquo;. </p>
          <p>And Gates was <strong>aware</strong> of his confinement because he was "apprehensive" of falling and "taunted" by the crowd. </p>
        </div>
      </div>
    </div>
      <div title="Page 69">
        <div>
          <div>
            <p>Kent may argue that Gates was not actually confined because all he had to do was go through the &ldquo;window&rdquo;. </p>
            <p>But that means of exit was <strong>not reasonably apparent</strong> because even the &ldquo;firemen&rdquo; who tried to get Gates down for &ldquo;hours&rdquo; did not realize discover it. </p>
          </div>
        </div>
      </div></td>
    <td valign="top"><div title="Page 69">
      <div>
        <div>
          <p>Therefore, Kent can be liable for false imprisonment.<br/>
          </p>
