    <td valign="top">4, 10, 11, 13</td>
    <td valign="top"><div title="Page 70">
      <div>
        <div>
          <p>Under tort law intentional infliction is an <strong>intentional, outrageous act</strong> causing the plaintiff severe emotional distress. Generally emotional distress will not be assumed, and the plaintiff must present evidence showing that they did, in fact, suffer severe emotional distress. Evidence of mere humiliation and embarrassment is generally not enough. </p>
        </div>
      </div>
    </div></td>
    <td valign="top"><div title="Page 70">
      <div>
        <div>
          <p>Here there was an <strong>intentional, outrageous act</strong> because Kent "flew" Gates to the top of a building and strand him there in front of a crowd where he was &ldquo;taunted for hours&rdquo;. </p>
          <p>However there is no evidence Gates suffered <strong>severe emotional distress</strong> because the facts only state that he was "humiliated" and &ldquo;embarrassed&rdquo;. </p>
        </div>
      </div>
    </div></td>
    <td valign="top"><div title="Page 70">
