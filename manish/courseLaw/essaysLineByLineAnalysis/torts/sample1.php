    <td valign="top">5. Trespass to Land</td>
    <td valign="top">6, 7, 14, 15</td>
    <td valign="top"><div title="Page 70">
      <div>
        <div>
          <p>Under tort law a trespass to land is an <strong>intentional act causing unauthorized entry</strong> onto, under or over the <strong>land of another.</strong> The entry may be by the <strong>defendant personally</strong> or by some <strong>person</strong> or <strong>object</strong> directed or placed on the land by the defendant. When an object is placed on the land it constitutes a &ldquo;continuing trespass.&rdquo; No damage is necessary, but the defendant is liable for all damage actually caused. And if malice can be shown the defendant may be liable for punitive damages. </p>
        </div>
      </div>
    </div></td>
    <td valign="top"><div title="Page 70">
      <div>
        <div>
          <p>Here Kent was <strong>over</strong> the land of the World Tribune because he "hovered" over the "building." And he caused Gates to go<strong> onto the land </strong>because he put Gates on the "ledge." </p>
          <p>This caused actual <strong>damage</strong> because Gates accidentally caused a "panel to fall." </p>
          <p>Additional damages are implied by the fact &ldquo;firemen&rdquo; worked in the building &ldquo;for hours&rdquo; trying to rescue Gates. This disruption probably caused Tribune to suffer lost wages and lost profits. </p>
          <p>Further, the possibility of malice is implied by the fact that Kent worked for one newspaper, the Daily Planet, and he disrupted business activities at what appears to be a competing newspaper, the World Tribune. </p>
        </div>
      </div>
    </div></td>
    <td valign="top"><div title="Page 70">
      <div>
        <div>
          <p>Therefore Kent would be liable for all damages he actually caused Tribune based on trespass to land, and may also be liable for punitive damages if implied malice could be proven. </p>
        </div>
