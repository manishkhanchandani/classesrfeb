<h1>Torts Sample Question 1 (Intentional Torts)</h1>

<div>
  <p>Clark Kent, star reporter for the Daily Planet was approached by panhandler Bill Gates and asked for a quarter. As a prank, Kent (who was secretly Superman) lifted and very gently flew Gates hundreds of feet up to the top of the World Tribune building and left Gates there on a ledge. </p>
  <p>Kent hovered over the World Tribune building and never touched the surface of the building. During the flight Gates was apprehensive he might fall, but Kent had a strong grip. </p>
  <p>Gates was embarrassed and the crowd below taunted him for hours as the fire department tried to devise a means of rescue. Gates was humiliated. </p>
  <p>As he stood on the ledge Gates accidentally knocked a decorative panel loose. It fell to the ground and injured fireman Frank. </p>
  <p>Suddenly Gates discovered that there was an unlocked window next to him all the time. He opened the window and escaped immediately. </p>
  <p>Discuss Kent's liability to Gates, the World Tribune and fireman Frank. </p>
</div>

<div>
      <p>[<strong>ANSWER EXPLANATION</strong>: This question has all of the common law intentional torts except for conversion and trespass to chattels. Why did Kent act? Because he intended to pull a &ldquo;prank.&rdquo; That fact expressly proves his acts were intentional. His physical acts imply intent, but the fact he wanted to pull a &ldquo;prank&rdquo; expressly proves intent. That saves you time. </p>
      <p>Be prepared for batteries that cause &ldquo;offense&rdquo; but not harm. Being pushed around by a stranger is offensive to a reasonable person, and be prepared to state that. </p>
      <p>The whole purpose of having fireman Frank hurt is to raise the issue of transferred intent. Did you spot the issue? Don&rsquo;t turn to &ldquo;negligence&rdquo; when the acts causing harm are intentional. </p>
      <p>If Frank was injured as the result of negligence by Kent or Gates, he might have no case because of the &ldquo;Fireman&rsquo;s Rule&rdquo; that rescue personnel cannot sue for injuries suffered as a result of the negligence of others that creates the emergency situation to which they have responded. That is often codified in statute, and it expressly recognizes the issue that emergency personnel, by occupation, have assumed the risks of their employment. </p>
      <p>But the Fireman&rsquo;s Rule does not generally extend to injury caused by intentional torts. </p>
      <p>Some professors insist that students &ldquo;always discuss defenses&rdquo;, and if your professor insists on that humor the prof and discuss defenses, even when there are no facts to suggest any. Often this sort of professor also wants the defenses discussed at the end of the essay in a &ldquo;Defenses&rdquo; section. </p>
      <p>But generally the writers of law exam and bar exam questions will guide you by giving you facts to suggest the issues you are to discuss, and if you are not given any facts that suggest an issue it means they don&rsquo;t want you to discuss that issue. </p>
      <p>And generally question writers want and expect you to discuss the issues in the order in which they arise in the fact pattern. </p>
      <p>If no facts are presented to suggest any plausible defense the writer is effectively saying, &ldquo;I don&rsquo;t want you to discuss defenses!&rdquo; Here there are absolutely no facts to suggest that Kent has any plausible defense claims, so I didn&rsquo;t waste my time discussing it.] </p>
    </div>

