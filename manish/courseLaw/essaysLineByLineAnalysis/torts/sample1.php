  <li>but Kent had a strong grip.<strong> (defense by Kent)</strong></li>
  <li>Gates was embarrassed <strong>(IIED)</strong></li>
  <li>and the crowd below taunted him for hours <strong>(aware of confinement)</strong>, <strong>(IIED)</strong></li>
  <li>as the fire department tried to devise a means of rescue. <strong>(confinement, not easy to rescue)</strong></li>
  <li>Gates was humiliated.<strong> (IIED)</strong></li>
  <li>As he stood on the ledge Gates accidentally knocked a decorative panel loose.  <strong>(TPL - damage)</strong></li>
  <li>It fell to the ground and injured fireman Frank. <strong>(TPL - damage)</strong>, <strong>(TI - Battery to Fireman because of Gates)</strong></li>
  <li>Suddenly Gates discovered that there was an unlocked window next to him all the time. <strong>(false imprisonment, can escape, but not apparent)</strong></li>
  <li>He opened the window and escaped immediately. <strong>(defense of Kent for FI)</strong></li>
  <li>Discuss Kent's liability to Gates, the World Tribune and fireman Frank. </li>
</ol>
