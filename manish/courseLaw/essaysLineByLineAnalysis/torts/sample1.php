        <div>
          <p>Battery is defined above. </p>
          <p>Under tort law transferred intent means that a defendant that <strong>acts with intent </strong>to commit any tort injury on anyone will be liable for every injury inflicted on everyone, even if the person injured or the injury suffered is not what was originally intended. </p>
          <p>Some Courts limit the application of transferred intent, but it is more likely to be applied when the original tortious act was intended to inflict harm. </p>
        </div>
      </div>
    </div></td>
    <td valign="top"><div title="Page 71">
      <div>
        <div>
          <p>Here Kent <strong>intentionally acted</strong> to cause Gates to suffer assault, battery, false imprisonment and possibly intentional infliction of emotional distress, and he <strong>intended</strong> to cause a trespass to the land of the World Tribune.</p>
          <p> In turn, that caused Frank to suffer a &ldquo;harmful touching&rdquo;. </p>
        </div>
      </div>
    </div></td>
    <td valign="top"><div title="Page 71">
      <div>
        <div>
          <p>Therefore, Kent would be liable to Frank for battery by transferred intent. </p>
