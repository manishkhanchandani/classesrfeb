<h1>Torts Sample Question 2 (Negligence)
</h1>

<div>
      <p>Tom bought some firecrackers in Rural County, where they were legal. He took them into the National Forest where federal law prohibited the possession and use of fireworks. The federal law was enacted to reduce the threat of forest fires and injury on national lands. </p>
      <p>In the National Forest Tom waded to a gravel bar in the middle of Big River, and there he carefully lit and threw the firecrackers into the air above the river. Occasionally one would fail to explode and it would fall harmlessly into the water. There was no one else around, and there was nothing on the gravel bar that could burn. </p>
      <p>Little Dick was playing a half-mile upstream from Tom, throwing sticks into Big River. He was three years old. His mother warned him to stay back from the edge of the river because it was dangerous. Dick disobeyed his mother and recklessly pushed a rotted tree into the river and it swirled away in the water. No one was hurt. </p>
      <p>Half a mile downstream from Tom, Paula was one of five people on the bridge above Big River fishing for trout. Paula was outside the National Forest boundaries. She had ignored a sign that said "No Fishing From Bridge." Fishing from the bridge was prohibited for traffic safety, but she was well off the roadway and there were very few cars this time of year. Paula was sitting carelessly on the edge of the bridge, but it was a nice day, the water was only six feet below her, and she knew how to swim. </p>
      <p>Tom threw another firecracker into the air and it did not explode. Instead it fell onto the log that Dick had pushed into the river upstream. </p>
      <p>The log swept down the river for half a mile before the firecracker exploded just as the log swept under the bridge. </p>
      <p>Paula was startled and fell into the river. </p>
      <p>Other people that had been fishing rushed off the bridge and down the river bank to rescue Paula as she laughingly climbed up the bank. </p>
      <p>Harry had been fishing too, but instead of going to help Paula, he stayed on the bridge and took two dollars out of Paula's purse. No one saw Harry take the money and the crime was never solved. Paula was hurt and upset. </p>
      <p>Under what theories can Paula seek to recover from Tom and Dick and what defenses would they raise? </p>
    </div>


<div>
      <p>[<strong>ANSWER EXPLANATION</strong>: This question focuses on the outer limits of DUTY to a remotely situated plaintiff (with firecracker yet, a la Palsgraf) and PROXIMATE CAUSATION of damage by some burning object floating down a river (a la Wagon Mound I and II). The question also focuses on the outer limits of BREACH because one cannot be breaching a duty by an act that poses no foreseeable risk to others. </p>
      <p>Law schools tend to brainwash every student into thinking they can always sue, and win, for every injury. The truth is that Paula&rsquo;s case is frivolous. ] </p>
    </div>

