<h1>Torts Sample Question 3 (Product Liability)
</h1>

<div>
      <p>The Macho-X99 chainsaw is a light-duty chainsaw designed for trees no bigger than 18". In the owner's manual said in big red letters, "WARNING -- NEVER TRY TO CUT DOWN A TREE BIGGER THAN 18" OR THIS SAW MIGHT CATCH FIRE." There were no warnings on the saw itself. </p>
      <p>Tom went into Sam's Bargain Center. Sam told him, "This is the best chainsaw on the market. The Macho-X99 will cut trees up to 36" in diameter." Tom bought the Macho-X99 chainsaw. </p>
      <p>Dick went into Sam's Bargain Center and said he didn't know much about chainsaws but needed one that could cut a tree about 24" in diameter. Sam told him, "I am an expert when it comes to chainsaws. I recommend this Macho-X99." Dick bought the Macho-X99 chainsaw. </p>
      <p>Moe went into Sam's Bargain Center and bought the Macho-X99 chainsaw without any discussion. </p>
      <p>Sam, Tom and Dick never read the owner's manual and were unaware of the potential fire danger. When Tom and Dick tried to cut down trees bigger than 18" the saws burst into flames and burned them. </p>
      <p>Moe saw the warning in the owner's manual and tried to return the saw. Sam refused to give Moe his money back. Moe was disgusted and threw the saw in the garbage &ldquo;Dumpster&rdquo;. </p>
      <p>Harry saw Moe's old saw in the garbage and took it. There was no owner's manual, but the saw was just like brand new. When he first used the saw it burst into flames and burned him. </p>
      <p>Discuss all the theories under which Tom, Dick and Harry would seek to recover from Sam. Under which theories can they NOT recover? </p>
    </div>


    <div>
      <p>[<strong>ANSWER EXPLANATION:</strong> This question calls for a discussion of the four theories of product liability. The facts are intended to illustrate the differences between express and implied breach along with the fact that strict product liability theory extends to remote plaintiffs. </p>
      <p>The preliminary discussion of whether this is an &ldquo;unreasonably dangerous product&rdquo; or not is very, very important. Stating the &ldquo;balance&rdquo; test given here is MANDATORY if you want to score maximum points. </p>
      <p>Be wary of products that are not really &ldquo;unreasonably dangerous&rdquo; at all because they have been extensively tested, have great utility (often saves lives), pose trivial risks (turns hair green) to a very small number of users (less than 0.05%), and what little danger they pose is totally unknown to the manufacturer or simply cannot be easily eliminated. That is NOT an unreasonably dangerous product! </p>
      <p>As for Harry, it is not clear if a person can prevail on a products liability claim if the product has been discarded, scrapped or abandoned. The purpose of product liability law is to protect consumers and regular users of products, not &ldquo;Dumpster Divers&rdquo;. There is a strong argument that a product is no longer in the &ldquo;stream of commerce&rdquo; after it has been discarded or scrapped. </p>
      <p>Here it seems that Harry put the product back in the stream of commerce by retrieving it from the Dumpster. Also he has assumed the risk by doing that since he knew or should have known something might be wrong with a product that has been discarded.] </p>
    </div>
 
