<h1>Torts Sample Question 4 (Defamation)
</h1>

<div>
      <p>Barbara, the famous TV news lady, was assigned to do a story on former governor, Pat Wilson, to find out why Wilson had become almost a recluse in the years since he left office. </p>
      <p>Barbara interviewed Wilson at his home. Wilson's speech was slurred, his eyes were bloodshot and he was unsteady. Barbara asked him about his health, and he declined to comment. Barbara knew Wilson had once been an alcoholic, and she suspected he had begun drinking heavily again. But she did not ask him about this because she knew he would deny it. </p>
      <p>On TV Barbara accurately described Wilson's slurred speech, bloodshot eyes and unsteady gait. Then she stated, "It appears that someone we once knew and respected has gone back to his old ways." She did not state who the "someone" was, and she did not explain what she meant by "his old ways." </p>
      <p>Because of the news broadcast, rumors rapidly spread that Wilson had again developed a drinking habit. Wilson's approval in polls fell dramatically, and he was passed over for the post of State Republican Chair, but he had little chance of being selected for that post. </p>
      <p>Wilson demanded a retraction and apology, but Barbara refuses. Discuss potential action by Wilson. </p>
    </div>


        <div>
          <p>[<strong>ANSWER EXPLANATION:</strong> This question requires discussion of defamation actions by public figures. It is an example of defamation using innuendo, interpretation (COLLOQUIUM) or knowledge of extrinsic facts (INDUCEMENT). </p>
          <p>A definition and discussion of actual malice is necessary. Actual malice may be based on recklessness. When it comes to recklessness, this question is a wobbler because Barbara clearly could have asked and then reported "Wilson denies a drinking problem." </p>
          <p>If Wilson was not a public figure, he would only have to prove the falsity of Barbara&rsquo;s statements and that they were of the sort that would damage his reputation. Barbara might then claim she was PRIVILEGED to make the statements, even if they were false. But she would have to prove she acted in a &lsquo;reasonable manner&rsquo; without malice to protect the public interest. But she would not be able to prove that because it is not clear what &ldquo;public interest&rdquo; she is protecting, and it would not be reasonable to make damning statements of this type on TV about a private individual without at least questioning the person about it first directly. </p>
          <p>PRIVILEGE is frequently tested and poorly taught so be sure to consider it in any defamation question. Did the defendant have a legitimate reason to speak out, and did they do it in a reasonable manner? The 1st Amendment does not protect defamatory statements, but it does protect the right to speak out to protect legitimate interests, as long as the defendant acts reasonably and without malice. Privilege defines the balance between these two conflicting rules of law.] </p>
        </div>

