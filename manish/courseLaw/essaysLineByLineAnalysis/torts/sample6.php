<h1>Torts Sample Question 6 (Nuisance)</h1>

<div>
      <p>Ken was a politician from Los Angeles. He moved to Sacramento and purchased a spacious home on the Sacramento River. He felt he got a great deal because he only paid $400,000 and a similar home in Los Angeles would have cost him three times that amount. </p>
      <p>Ken realized his home was directly in the flight path of the airplanes taking off and landing from the County airport, and the realtor had prominently disclosed this fact on the sales documents. But Ken didn't think the noise was so bad because from his backyard he just loved to hear little birds singing along the public river parkway. </p>
      <p>Four years later Ken lost the election and he wanted to move back to Los Angeles. When he had the house appraised, it was worth $500,000. Ken was furious because his home value had only increased 25 percent in value while most real estate had gone up 50 percent. </p>
      <p>Ken blamed the County. In the four years he owned the home the number of flights at the airport increased ten percent. This increased traffic was from increased military use in response to the crisis in Romaria. Ken could not enjoy his yard as much as before, and the little birds on the public river parkway didn't seem to sing as much as they used to. </p>
      <p>What actions might Ken bring against the County, what defenses might be raised, and what remedies are appropriate? </p>
    </div>


        <div>
          <p>[<strong>ANSWER EXPLANATION:</strong> This question is roughly based on true events. The point to remember is that "coming to the nuisance" is not a complete bar to recovery. If a defendant unreasonably interferes with the use of land, a new purchaser might "buy into the right to sue" for a nuisance the previous owner did not litigate. </p>
          <p>Just state the rule and prove (or disprove) the elements. Here Ken&rsquo;s case is rather weak, so you may have to give &ldquo;conditional&rdquo; conclusions. </p>
          <p>Discussion of &ldquo;defenses&rdquo; and &ldquo;remedies&rdquo; is mandatory because the CALL specifically asks for it.] </p>
        </div>

