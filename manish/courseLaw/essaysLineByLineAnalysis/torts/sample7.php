<h1>Torts Sample Question 7 (Miscellaneous Torts)
</h1>

<div>
      <p>Star approached Buck, the owner of JavaManiac, with a business proposal. Star knew where there was a retail space for lease that would be a great place for a JavaManiac franchise. Buck told Star that she would be granted a JavaManiac license if the location was acceptable. Based on this Star described the location in detail and gave Buck its address. Buck said he would have someone "check it out." </p>
      <p>Buck had no intention of giving Star a franchise. He just wanted her to reveal the location. Based on Star's description it sounded perfect for a JavaManiac outlet, and Buck wanted to take the space himself. </p>
      <p>Buck went to the location suggested by Star and saw that it was perfect for JavaManiac. He then contacted the owner, Jack. Jack said he already had tentatively promised the lease to someone named Star. Buck said, "I know Star," and held his hand in the air with his thumb out to indicate that Star had a drinking problem. Then Buck looked at Jack very sincerely and said, "I probably shouldn't say this, but I feel I have a duty to tell you that you don't want to lease to someone like Star." </p>
      <p>Buck reported to Star that the location was unacceptable because it was too small to meet the secret JavaManiac minimum guidelines. </p>
      <p>Star was very dejected and withdrew her offer to lease the space. Jack was relieved. </p>
      <p>Three months later Star happened upon the grand opening of the next new JavaManiac franchise at the very location he had suggested. Standing in front were Buck and Jack shaking hands. Star was furious and accused Buck of cheating her. Buck rolled his eyes at Jack and said, "See what I mean?" </p>
      <p>Then Buck sued Star for slander per se for accusing him of questionable business practices. Star won the suit when the jury found her statements had been true. </p>
      <p>What are Star's possible actions against Buck? (Do not discuss intentional infliction, defamation or false light.) </p>
    </div>


        <div>
          <p>[<strong>ANSWER EXPLANATION: </strong>This very short question is intended to focus on the elements that must be proven for fraud, interference, and abuse of process. </p>
          <p>Intentional infliction, defamation and false light were excluded by the call because they are covered in other questions. </p>
          <p>There is no evidence of IIED anyway because Star did not suffer &ldquo;severe emotional distress.&rdquo; You are unlikely to ever get such a short, simple question on a real exam.] </p>
        </div>

