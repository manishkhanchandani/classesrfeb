<p><strong>Should Al, Bob or Charlie be convicted of: </strong></p>
<p><strong> a. Conspiracy to violate the Tariff Act? </strong></p>
<p><strong>1. Conspiracy</strong></p>
<p>Under Common Law, the crime of Conspiracy was an agreement between two or more people to work toward an illegal goal. Modernly Overt Act toward the criminal goal is often required.Conspiracy does not merge into ultimate crime.</p>
<p>Further, under Pinkerton's rule, a member of conspiracy is<strong> Vicariously Liable</strong> for the criminal acts of co-conspirators done within the scope of conspiracy goal. This means crimes that were 1) foreseeable and 2) in furtherence of conspiracy goal. </p>
<p>Here Al, Bob, Charlie <strong>agreed </strong>to do an illegal goal because Al, Bob, Charlie planned to bring 50 cases of wiskey. Here it is an <strong>illegal goal</strong> because they knew that smuggling items into this country without paying duty required by Tariff Act is a crime. Here they did overt act when Al stole the truck and went to ashore to get the whiskey. </p>
<p>Under pinkertons rule, Al, Bob, Charlie are the members of the conspiracy and they are <strong>vicariously liable f</strong>or the crimes of each other which are foreseeable and in furtherence of conspiracy goal. Here violation of tariff act was <strong>foreseeable</strong> because they planned to take wiskey by smuggling it without paying the duty. </p>
<p>Therefore they would be charged with crime of conspiracy. </p>
<p>2. Larceny (Theft) of Truck?</p>
<p>Under common law, Larceny was the Trespassory taking and carrying away of the personal property of another with intent to permenantly deprive.</p>
<p>Here there is trespassory of truck because nobody gave permission to them to take the truck and get the wiskey. </p>
