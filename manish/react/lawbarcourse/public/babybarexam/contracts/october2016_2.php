Owner wants to turn her warehouse into a restaurant. She decides to install an
innovative solar heating system, which Contractor agrees to install at a cost of $50,000.
Contractor’s son (“Son”) wants to use two parking spaces in the warehouse parking lot
for his (Son’s) business. If Owner agrees to designate two parking spaces for Son’s
use for five years, Contractor will drop the price to $35,000.<br><br>
On November 13th, the parties agree to the latter arrangement in a valid written contract
in which Contractor promises to start the job on November 17th and to complete it by
January 1st. The contract includes a recital stating, “Timely performance by Contractor
is important to avoid any delay in the opening of Owner’s restaurant.”<br><br>
State law requires that all installations of the new solar systems be done by a certified
solar technician. On November 15th
, the only certified technician who works for
Contractor, Tech, is injured in a car accident. Contractor immediately notifies Owner
and advises her that the start of the work will be delayed because of Tech’s accident.
Owner replies, “You know that on-time performance is crucial. Yesterday, the city
announced special tax breaks for businesses that open by the end of the year. Can you
still finish by then?” Contractor says, “I don’t know when we can start. It depends on
how quickly Tech recovers.” Owner tells Contractor that she is terminating the contract.<br><br>
Owner finds an alternative supplier of a similar system at a cost of $60,000, but he can’t
start work immediately and the restaurant opens in February of the following year.
Owner misses the deadline for the city tax break.<br><br>
Owner sues Contractor for breach of contract. Son sues Owner for breach of contract,
seeking damages for Owner’s failure to provide the two parking spaces.<br><br>
